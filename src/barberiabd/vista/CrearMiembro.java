    private void crear_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crear_btnActionPerformed
        int validacion = 0, validacionB = 0, tipo_cbm;
        nombre = nombre_tf.getText().trim();
        direccion = direccion_tf.getText().trim();
        id = Integer.parseInt(id_tf.getText().trim());
        String telefono_string = tel_tf.getText().trim();
        contraseña = pass_tf.getText().trim();
        tipo_cbm = tipoNivel_cmb.getSelectedIndex();

        if (nombre.equals("")) {
            nombre_tf.setBackground(Color.red);
            validacion++;
        }
        if (direccion.equals("")) {
            direccion = null;
        }
        if ((Integer.toString(id)).equals("")) {
            id_tf.setBackground(Color.red);
            validacion++;
        }
        if (contraseña.equals("")) {
            pass_tf.setBackground(Color.red);
            validacion++;
        }
        if (tipo_cbm == 1) {
            tipo_String = "Administrador";
        } else {
            tipo_String = "Barbero";
        }
        if (tipo_String.equals("Administrador") && validacion == 0) {
            if (telefono_string.equals("")) {
                int telefono = 0;
            }
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "select id from administrador where id = '" + id + "'");
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    id_tf.setBackground(Color.red);
                    JOptionPane.showMessageDialog(null, "Identificacion ya existente");
                    cn.close();
                } else {
                    cn.close();
                    if (validacion == 0) {
                        try {
                            Connection cn2 = Conexion.conectar();
                            PreparedStatement pst2 = cn2.prepareStatement(
                                    "insert into administrador values (?,?,?,?,?)"); //Se agregan los datos a la base de datos

                            pst2.setInt(1, id);
                            pst2.setString(2, nombre);
                            pst2.setString(3, direccion);
                            pst2.setInt(4, telefono);
                            pst2.setString(5, contraseña);

                            pst2.executeUpdate();
                            cn2.close();

                            Limpiar();

                            nombre_tf.setBackground(Color.green);
                            id_tf.setBackground(Color.green);
                            direccion_tf.setBackground(Color.green);
                            tel_tf.setBackground(Color.green);
                            pass_tf.setBackground(Color.green);

                            JOptionPane.showMessageDialog(null, "Registro exitoso");

                        } catch (SQLException e) {
                            System.err.println("Error en registrar administrador" + e);
                            JOptionPane.showMessageDialog(null, "Error al registrar, contacte al desarrollador.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Debe llenar todos los campos obligatorios.");
                    }
                }
            } catch (SQLException e) {
                System.err.println("Error en validar id del usuario." + e);
                JOptionPane.showMessageDialog(null, "Error al comparar usuario");
            }
        }

        if (validacion == 0 && tipo_String.equals("Barbero")) {
            if (telefono_string.equals("")) {
                tel_tf.setBackground(Color.red);
                validacionB++;
            }
            if ((maquinaAsig_tf.getText().trim()).equals("")) {
                maquinaAsig_tf.setBackground(Color.red);
                validacionB++;
            }
            if ((porcentajeCom_tf.getText().trim()).equals("")) {
                porcentajeCom_tf.setBackground(Color.red);
                validacionB++;
            }
            if (validacionB == 0) {
                maquinaAsignada = Integer.parseInt(maquinaAsig_tf.getText().trim());
                porcentajeComision = Integer.parseInt(porcentajeCom_tf.getText().trim());
                telefono = Integer.parseInt(telefono_string);
                try {
                    Connection cn = Conexion.conectar();
                    PreparedStatement pst = cn.prepareStatement(
                            "select id from barbero where id = '" + id + "'");
                    ResultSet rs = pst.executeQuery();
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "Identificacion ya existente");
                        cn.close();
                    } else {
                        if (validacion == 0) {
                            try {
                                Connection cn2 = Conexion.conectar();
                                PreparedStatement pst2 = cn2.prepareStatement(
                                        "insert into barbero values (?,?,?,?,?,?,?)"); //Se agregan los datos a la base de datos

                                pst2.setInt(1, id);
                                pst2.setString(2, nombre);
                                pst2.setString(3, direccion);
                                pst2.setInt(4, telefono);
                                pst2.setInt(5, maquinaAsignada);
                                pst2.setInt(6, porcentajeComision);
                                pst2.setString(7, contraseña);

                                pst2.executeUpdate();
                                cn2.close();

                                JOptionPane.showMessageDialog(null, "Registro exitoso");

                            } catch (SQLException e) {
                                System.err.println("Error en registrar Barbero" + e);
                                JOptionPane.showMessageDialog(null, "Error al registrar, contacte al desarrollador.");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos.");
                        }
                    }
                } catch (SQLException e) {
                    System.err.println("Error en validar id del usuario." + e);
                    JOptionPane.showMessageDialog(null, "Error al comparar usuario");
                }
            }
        }
    }//GEN-LAST:event_crear_btnActionPerformed

